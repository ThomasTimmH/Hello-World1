public class Personer {
private String name;
private int age;
private double weight;
private double height;

public Personer(String name, int age, double weight,double height){
    this.name=name;
    this.age=age;
    this.weight=weight;
    this.height=height;
}
public String getName(){
    return name;
}
public double getHeight(){
    return height;
}



}
